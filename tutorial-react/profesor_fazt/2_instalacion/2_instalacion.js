/*

																_____Instalación_____


 - Para instalar React devemos ir al siguiente link:
   https://reactjs.org/

 - Dicho link también la documentación oficial de React. Aunque dicha documentación este en varios idiomas, recordar que la más actualizada esta en ingles  



 - Primero devemos intalar "Create React App", esta es una herramienta para utilizar React (los pasos y los comandos de instalación entan en el mismo link)

	- Primero iremos a la terminal. En caso de usar la terminal de Visual Studio Code eligiremos la opcion de "Comand Promp". Esto hace que se ejecute tal cual como 
	  si fuera la terminal de windows. 

	- Luego escribiremos la palabra "cd" segido de la direccion de la carperta en la cual queremos hacer la instalación. Esto hara que nos posicionemos en dicha 
	  carpeta desde la terminal.
	  Ej: 	cd C:\Users\Nicolas\Desktop\Cursos - Front End - Dorian Desing\tutorial-react\profesor_fazt\2_instalacion   

	- Una vez posicionado en la carpeta deseada, tenemos que crear la aplicación React. Para ello escribiremos lo siguiente en la terminal:
	  npx create-react-app my-app

	- Con esto tenemos instalado diferentes archivos de React nuestra carpeta. Ahora solamente falta editarlos. A su vez no va a dar comandos para la terminal:
		- npm start						Inicia el servidor en el navegador y nos permite comenzar el desarrollo
		- npm build						Nos permite convertir el codigo de desarrollo en codigo de producción. Esto es necesario debido a que los navegadores 
										solamente leen codigo HTML, CSS y JS. Por ende, una vez terminado el desarrollo, hay que convertir dicho codigo React en 
										codigo JS
										
	- En la terminal tenemos que volver a posicionarnos, pero ahora lo hacemos en la nueva carpeta "my-app" que se creo, ya que es ahí donde se hubica nuestra 
	  aplicación React. 

	- En la terminal escribimos: 
	  npm start
	  Acto seguido se habrira nuestro proyecto React en el navegador. 
	  Debemos hacer esto cada vez que queramos habrir nuestro proyecto de React. Recordar que con aterioridad hay que seleccionar la opción "Command prompt" en caso 
	  de usar la terminal de "Visual Studio Code" y tambien de posicionarse en la carpeta "my-app"
	  
	- Para trabajar debemos usar los archivos que se instalaron en la carpeta  


 - A trabajar con React!


*/